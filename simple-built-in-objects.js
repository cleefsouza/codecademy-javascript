/*
* simple built-in-objects
*/
console.log(Math.random() * 100); // número aleatório em decimal
console.log(Math.floor(Math.random() * 100)); // arredonda para o próximo número inteiro
console.log(Math.ceil(43.8)); // retorna o menor inteiro menor ou igual ao número
console.log(Number.isInteger(2017)); // verifica se o número é inteiro